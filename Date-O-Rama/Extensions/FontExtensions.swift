import Foundation
import SwiftUI

extension Font {
    static var headlineMonospacedDigit:  Font {
        return Font.headline.monospacedDigit()
    }
    
    static var subheadlineMonospacedDigit:  Font {
        return Font.subheadline.monospacedDigit()
    }
}
